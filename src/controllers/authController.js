import bcrypt from "bcryptjs";
import jwt from "jsonwebtoken";
import User from "../models/User.js";

/**
 * üìù Registro de usuario desde el frontend
 */
export const registerUser = async (req, res) => {
  try {
    const { nombre, email, password, role } = req.body;

    if (!email || !password || !role || !nombre) {
      return res
        .status(400)
        .json({ message: "Todos los campos son obligatorios" });
    }

    const existingUser = await User.findOne({ email });
    if (existingUser) {
      console.warn(`‚ö†Ô∏è Intento de registro duplicado: ${email}`);
      return res.status(400).json({ message: "El usuario ya existe" });
    }

    const hashedPassword = await bcrypt.hash(password, 10);

    const newUser = new User({
      nombre,
      email,
      password: hashedPassword,
      role,
      isValidated: false,
    });

    await newUser.save();

    console.log(`‚úÖ Usuario registrado: ${email} (${role})`);
    res.status(201).json({
      message:
        "Usuario registrado correctamente. Pendiente de validaci√≥n por el administrador.",
    });
  } catch (error) {
    console.error("‚ùå Error en el registro:", error.message);
    res.status(500).json({ message: "Error en el registro de usuario" });
  }
};

/**
 * üîê Login universal
 */
export const loginUser = async (req, res) => {
  try {
    const email = req.body.email || req.body.correo;
    const { password } = req.body;

    if (!email || !password) {
      return res.status(400).json({ message: "Email y contrase√±a requeridos" });
    }

    const user = await User.findOne({ email }).select("+password");
    if (!user) {
      console.warn(`‚ö†Ô∏è Login fallido: usuario no encontrado ‚Üí ${email}`);
      return res.status(404).json({ message: "Usuario no encontrado" });
    }

    if (!user.isValidated) {
      console.warn(`‚ö†Ô∏è Login bloqueado: cuenta no validada ‚Üí ${email}`);
      return res.status(403).json({
        message: "Cuenta pendiente de validaci√≥n por el administrador",
      });
    }

    const isMatch = await bcrypt.compare(password, user.password);
    if (!isMatch) {
      console.warn(`‚ö†Ô∏è Login fallido: contrase√±a incorrecta ‚Üí ${email}`);
      return res.status(401).json({ message: "Contrase√±a incorrecta" });
    }

    if (!process.env.JWT_SECRET) {
      console.error("‚ùå JWT_SECRET no est√° definido");
      return res
        .status(500)
        .json({ message: "Error interno de configuraci√≥n" });
    }

    const token = jwt.sign(
      {
        userId: user._id,
        email: user.email,
        role: user.role,
        isValidated: user.isValidated,
      },
      process.env.JWT_SECRET,
      { expiresIn: "1h" }
    );

    console.log(`‚úÖ Login exitoso: ${email} (${user.role})`);
    res.status(200).json({ message: "Login exitoso", token });
  } catch (error) {
    console.error("‚ùå Error en el login:", error.message);
    res.status(500).json({ message: "Error en el login" });
  }
};

/**
 * üì° Verifica sesi√≥n activa
 */
export const pingUser = async (req, res) => {
  try {
    const user = req.user;

    if (!user) {
      console.warn("üîí Ping fallido: usuario no autenticado");
      return res.status(401).json({ message: "Usuario no autenticado" });
    }

    res.status(200).json({
      message: "Sesi√≥n v√°lida",
      role: user.role,
      email: user.email,
      userId: user.userId,
    });
  } catch (error) {
    console.error("‚ùå Error en ping:", error.message);
    res.status(500).json({ message: "Error al verificar sesi√≥n" });
  }
};

/**
 * üõ†Ô∏è Creaci√≥n de usuario institucional desde el panel admin
 */
export const crearUsuarioDesdeAdmin = async (req, res) => {
  try {
    const { nombre, email, role, password } = req.body;

    if (!email || !role || !password || !nombre) {
      return res
        .status(400)
        .json({ message: "Nombre, correo, rol y contrase√±a son obligatorios" });
    }

    const existente = await User.findOne({ email });
    if (existente) {
      console.warn(`‚ö†Ô∏è Usuario ya existe: ${email}`);
      return res.status(409).json({ message: "El correo ya est√° registrado" });
    }

    const hashedPassword = await bcrypt.hash(password, 10);

    const nuevoUsuario = new User({
      nombre,
      email,
      role,
      password: hashedPassword,
      isValidated: true,
    });

    await nuevoUsuario.save();

    console.log(`‚úÖ Usuario creado desde admin: ${email} (${role})`);
    res.status(201).json({
      message: "Usuario creado correctamente",
      usuario: {
        nombre: nuevoUsuario.nombre,
        email: nuevoUsuario.email,
        role: nuevoUsuario.role,
      },
    });
  } catch (error) {
    console.error("‚ùå Error al crear usuario desde admin:", error.message);
    res.status(500).json({ message: "Error interno al crear usuario" });
  }
};
